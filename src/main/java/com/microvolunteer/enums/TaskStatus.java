package com.microvolunteer.enums;

/**
 * Enum representing different statuses of tasks in the system.
 */
public enum TaskStatus {
    /**
     * Task is open and available for volunteers
     */
    OPEN,
    
    /**
     * Task is currently being worked on by a volunteer
     */
    IN_PROGRESS,
    
    /**
     * Task has been completed
     */
    COMPLETED
}
